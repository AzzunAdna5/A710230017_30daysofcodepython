angka_1 = input('Masukkan angka 1: ')
angka_2 = input('Masukkan angka 2: ')

# Konversi input angka menjadi float
angka_1 = float(angka_1)
angka_2 = float(angka_2)

# Perhitungan hasil bagi dan modulus
hasil_bagi = angka_1 / angka_2
hasil_modulus = angka_1 % angka_2

# Menampilkan hasil
print('Hasil angka 1 dibagi angka 2 =', hasil_bagi)
print('Hasil modulus angka 1 dengan angka 2 =', hasil_modulus)
